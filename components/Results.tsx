const getRatingMessage = (rating: number) => {
  if (rating >= 4.5) return { text: 'Excellent!', color: 'text-green-600', bg: 'bg-green-50' };
  if (rating >= 4.0) return { text: 'Very Good', color: 'text-green-500', bg: 'bg-green-50' };
  if (rating >= 3.5) return { text: 'Good', color: 'text-yellow-600', bg: 'bg-yellow-50' };
  if (rating >= 3.0) return { text: 'Average', color: 'text-yellow-500', bg: 'bg-yellow-50' };
  if (rating >= 2.0) return { text: 'Needs Work', color: 'text-orange-500', bg: 'bg-orange-50' };
  return { text: 'Poor', color: 'text-red-500', bg: 'bg-red-50' };
};

